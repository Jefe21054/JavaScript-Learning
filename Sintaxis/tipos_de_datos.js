/**
 * TIPOS DE DATOS
 */

// NUMBER - Para todos los tipos de numeros. Enteros y decimales

// STRING - Cadena de Texto -> 'Ivan Iglesias' y 'a'

// BOOLEAN - true, false

console.log(typeof 50)
console.log(typeof 50.6)
console.log(typeof 'Ivan Iglesias')
console.log(typeof 'a')

/**
 * TIPOS DE DATOS - Coleecion de Datos
 */

/**
 * OBJETOS:
 * Colecciones de datos (informacion)
 * que tienen un identificador y un valor
 */

// ARRAY (ARREGLO) - [separados por ,]

console.log(typeof ['Ivan', 'Jose', 'Juan', 'Jean', 'Oto'])

/**
 * OBJETOS:
 * {
    nombre: 'Ivan',
    apellido: 'Iglesias'
    }
 */

console.log({
    nombre: 'Ivan',
    apellido: 'Iglesias'
})

// NULO - null

/**
 * TIPOS DE DATOS - No Definido
 */

console.log(typeof undefined)
