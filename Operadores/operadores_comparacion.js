/**
 * APLICA A NUMEROS, TEXTO Y BOOLEANOS
 * ES IGUAL? -> == (COMPARA SOLO VALOR)
 * ES ESTRICTAMENTE IGUAL? -> === (COMPARA VALOR Y TIPO DE DATO)
 * ES DIFERENTE? -> != (VERIFICA SI VALORES SON DIFERENTES)
 * ES ESTRICTAMENTE DIFERENTE? -> != (VERIFICA SI VALORES SON DIFERENTES)
 */

