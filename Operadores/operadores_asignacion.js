/**
 * OPERADORES DE ASIGNACION
 */

/**
 * =   -> ASIGNA UN VALOR
 * +=  -> ASIGNA Y SUMA UN VALOR
 * -=  -> ASIGNA Y RESTA UN VALOR
 * *=  -> ASIGNA Y MULTIPLICA UN VALOR
 * /=  -> ASIGNA Y DIVIDE UN VALOR
 * %=  -> ASIGNA Y SACA EL RESIDUO DE DIVIDIR DOS VALORES
 * **= -> ASIGNA Y ELEVA A UNA POTENCIA
 */

let a = 10
let b = 4

a += b
a -= b
a *= b
a /= b
a %= b
a **= b

console.log(a)